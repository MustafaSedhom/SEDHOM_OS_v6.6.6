inline SEDHOM_GFXglyph *pgm_read_glyph_ptr(const SEDHOM_GFXfont *gfxFont, U_int8_t c) {
#ifdef __AVR__
  return &(((SEDHOM_GFXglyph *)pgm_read_pointer(&gfxFont->glyph))[c]);
#else
  return gfxFont->glyph + c;
#endif
}

inline U_int8_t *pgm_read_bitmap_ptr(const SEDHOM_GFXfont *gfxFont) {
#ifdef __AVR__
  return (U_int8_t *)pgm_read_pointer(&gfxFont->bitmap);
#else
  return gfxFont->bitmap;
#endif
}
